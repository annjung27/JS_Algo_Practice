// Directions
// Return a string with the order of characters reversed
// --- Examples
//   reverse('abcd') === 'dcba'
//   reverse('Hello!') === '!olleH'

function reverse(str) {
  /* solution 1 (using built in methods)
1. turn the string into an array (use .split(''))
2. use array method of reverse() to reverse contents of array
3. turn the reversed array back into a string (using .join(""))
  //   let arr = str.split("");
  //   arr.reverse();
  //   const res = arr.join("");
  //   return res;
*/

  /* Solution 2 - solve without using built in methods
1. create empty string variable.
2. iterate through the str using for loop
3. concat each letter (str[i]) and result string.
*/
  let res = "";

  for (let i = 0; i < str.length; i++) {
    const letter = str[i];
    res = letter + res;
  }

  return res;
}

/* 
Time Complexity : O(N)  We go over every character in Input String
Space Complexity : O(N)
*/

